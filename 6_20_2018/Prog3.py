#"range" function examples

for i in range(1, 10):
    print(i)


a = range(1, 10)
for i in a:
    print(i)
    
for a in range(21, -1, -2):
    print (a)


for i in range(0, 20, 2):
    print(i)

for i in range(20, 0, -2):
    print(i)

